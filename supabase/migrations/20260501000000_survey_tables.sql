-- ============================================================
-- 1. survey_responses（アンケート回答）
-- ============================================================
CREATE TABLE public.survey_responses (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  booking_id uuid NOT NULL REFERENCES public.bookings(id),
  customer_user_id bigint NOT NULL REFERENCES public.users(id),
  provider_id bigint NOT NULL REFERENCES public.providers(id),
  csat smallint NOT NULL CHECK (csat BETWEEN 1 AND 5),
  driver_service smallint CHECK (driver_service BETWEEN 1 AND 5),
  driver_quality smallint CHECK (driver_quality BETWEEN 1 AND 5),
  driver_price smallint CHECK (driver_price BETWEEN 1 AND 5),
  comment text,
  review_text text,
  review_public boolean NOT NULL DEFAULT false,
  review_visible boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE UNIQUE INDEX survey_responses_booking_id_key ON public.survey_responses USING btree (booking_id);
CREATE INDEX idx_survey_responses_provider ON public.survey_responses USING btree (provider_id);
CREATE INDEX idx_survey_responses_customer ON public.survey_responses USING btree (customer_user_id);

-- ============================================================
-- 2. survey_template_questions（業界別テンプレート設問マスタ）
-- ============================================================
CREATE TABLE public.survey_template_questions (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  category text NOT NULL,
  question_text text NOT NULL,
  sort_order integer NOT NULL DEFAULT 0,
  is_active boolean NOT NULL DEFAULT true,
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE INDEX idx_survey_template_questions_category ON public.survey_template_questions USING btree (category);

-- ============================================================
-- 3. provider_survey_settings（事業主が選んだ追加テンプレート設問）
-- ============================================================
CREATE TABLE public.provider_survey_settings (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  provider_id bigint NOT NULL REFERENCES public.providers(id),
  template_question_id bigint NOT NULL REFERENCES public.survey_template_questions(id),
  custom_text text,
  sort_order integer NOT NULL DEFAULT 0,
  created_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (provider_id, template_question_id)
);

CREATE INDEX idx_provider_survey_settings_provider ON public.provider_survey_settings USING btree (provider_id);

-- ============================================================
-- 4. pending_survey_notifications（送信キュー）
-- ============================================================
CREATE TABLE public.pending_survey_notifications (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  booking_id uuid NOT NULL REFERENCES public.bookings(id),
  customer_user_id bigint NOT NULL REFERENCES public.users(id),
  provider_id bigint NOT NULL REFERENCES public.providers(id),
  scheduled_at timestamptz NOT NULL,
  sent_at timestamptz,
  status text NOT NULL DEFAULT 'pending' CHECK (status IN ('pending', 'sent', 'failed')),
  created_at timestamptz NOT NULL DEFAULT now()
);

CREATE UNIQUE INDEX pending_survey_notifications_booking_id_key ON public.pending_survey_notifications USING btree (booking_id);
CREATE INDEX idx_pending_survey_status ON public.pending_survey_notifications USING btree (status, scheduled_at);

-- ============================================================
-- 5. customer_visit_counts（訪問回数管理）
-- ============================================================
CREATE TABLE public.customer_visit_counts (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  customer_user_id bigint NOT NULL REFERENCES public.users(id),
  provider_id bigint NOT NULL REFERENCES public.providers(id),
  visit_count integer NOT NULL DEFAULT 0,
  updated_at timestamptz NOT NULL DEFAULT now(),
  UNIQUE (customer_user_id, provider_id)
);

CREATE INDEX idx_customer_visit_counts_customer ON public.customer_visit_counts USING btree (customer_user_id);

